typedef long long ll;
ll MOD;
ll fpow(ll b, ll e){
	if(e<0)return 0;
	ll ret=1;
	while(e){
		if(e&1)ret=ret*b%MOD;
		b=b*b%MOD,e>>=1;
	}
	return ret;
}
ll r;
ll f(ll n){
	if(n==0)return 0;
	return (n%2*fpow(r,n-1)+(fpow(r,n/2)+1)*f(n/2))%MOD;
}
